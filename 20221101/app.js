// TypeScript
// JavaScript에서 타입 설정이 추가된 언어
// JavaScript에서 타입이 확장된 언어
// TypeScript는 javaScript의 상위 집합 슈퍼셋으로서
// 대형 프로젝트를 진행할 때 어느정도 약점, 오류를 잡는데 좋음

// C++, C# 같이 객체지향 프로그래밍을 지원하는데
// TypeScript는 객체지향 프로그래밍에 특화된 프로그래밍 패턴을 지원
// 현재는 함수형 프로그래밍이 대세라서 타입검사나 추론 등의 기능만 사용

// TypeScript를 쓰면 JavaScript로 작업할때 보다
// 개발에서 생기는 에러를 사전 방지 가능
// JavaScript의 코드의 품질과 개발 생산성 높이기 가능

// 코드의 완성 가이드
// JavaScript는 타입이 정해져 있지 않아서 자동완성이 미리 뜨지않아
// 일일이 타이핑 해야함
// TypeScript는 타입을 정해놔서 미리 자동완성이 뜨기 때문에
// 빠르고 정확하게 작업 가능

// TypeScript는 에러를 사전에 방지하는데(바로 확인 가능)
// JavaScript는 코드를 실행시켜야 에러 확인 가능

// TypeScript는 프로그래밍 언어
// TypeScript의 컴파일은 그냥 TypeScript코드를 JavaScript 코드로 바꿔주는 것
// 브라우저가 이해할 수 있는 언어가 JavaScript이기 때문

// 설치명령어
// npm i -g typescript

// 설치 확인
// tsc --version

// tsconfig.json 생성 명령어
// tsc --init

// tsconfig.json은 TypeScript의 설정파일 TypeScript의 설정값 조정가능

// TypeScript의 변수 타입 지정
// 변수명 : 타입 = 초기값

// JavaScript는 변수명 = 초기값

